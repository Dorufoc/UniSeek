# UniSeek 兼职招聘平台 - Mock 数据时间工具函数
# ==============================================================================
# 提供加权时间分布函数，使 Mock 数据在 2010-2026 年间均匀分布且近期密集，
# 以支持仪表盘趋势分析（近10年、近12个月、近30天、近7天、近24小时）。

import datetime
import random

# =============================================================================
# 时间段定义（带权重）
# =============================================================================
# 权重越高，该时间段内生成的数据点越多。
# 设计目标：
#   - 近10年（2016-2026）：各年份均有数据 ✅
#   - 近12个月（2025-07 至 2026-07）：约 45% 数据 ✅
#   - 近30天（2026-06 至 2026-07）：充足数据点 ✅
#   - 近7天（2026-07-08 至 2026-07-15）：每日活动 ✅
#   - 近24小时（2026-07-15）：小时级活动 ✅

TIME_SEGMENTS = [
    # (起始日期, 结束日期, 权重, 描述)
    # === 近10年趋势基础数据：2016-2024 每年均匀分布 ===
    
    # 2010-2015: 历史基线 - 6年，权重12 → ~2%/年
    ("2010-01-01", "2015-12-31", 12,   "2010-2015: 历史基线"),
    
    # 2016-2019: 增长期 - 4年，权重25 → ~6.25%/年
    ("2016-01-01", "2019-12-31", 25,   "2016-2019: 增长期"),
    
    # 2020-2024: 成熟期 - 5年，权重30 → 6%/年
    ("2020-01-01", "2024-12-31", 30,   "2020-2024: 成熟期"),
    
    # === 近期高密度数据：2025-2026 支撑各项看板 ===
    
    # 2025全年（1年），权重17
    ("2025-01-01", "2025-12-31", 17,   "2025年（近2年趋势）"),
    
    # 2026上半年（6个月），权重8 → 覆盖近12个月/近30天
    ("2026-01-01", "2026-06-30", 8,    "2026上半年（近12个月）"),
    
    # 2026年7月1-14日（14天），权重4 → 覆盖近7天/近30天
    ("2026-07-01", "2026-07-14", 4,    "近两周（每日粒度）"),
    
    # 2026年7月15日（1天），权重4 → 小时级粒度
    ("2026-07-15", "2026-07-15", 4,    "最后24小时（小时级）"),
]

# 缓存已解析的时间段
_PARSED = None


def _get_segments():
    """解析 TIME_SEGMENTS 中的日期字符串为 datetime 对象并缓存。"""
    global _PARSED
    if _PARSED is None:
        _PARSED = []
        for start_str, end_str, weight, desc in TIME_SEGMENTS:
            start = datetime.datetime.strptime(start_str, "%Y-%m-%d")
            # 结束时间取当天最后一秒
            end = (datetime.datetime.strptime(end_str, "%Y-%m-%d")
                   + datetime.timedelta(days=1)
                   - datetime.timedelta(seconds=1))
            _PARSED.append((start, end, weight, desc))
    return _PARSED


def weighted_random_time():
    """
    返回 2010-01-01 至 2026-07-15 之间的加权随机时间点。

    按时间段权重分布，确保近期数据更密集，支持各时间粒度的趋势分析。
    适合用于创建时间、日志时间等所有需要时间戳的字段。

    Returns:
        datetime.datetime: 加权随机时间点
    """
    segments = _get_segments()
    weights = [s[2] for s in segments]

    # 按权重随机选择一个时间段
    segment = random.choices(segments, weights=weights, k=1)[0]
    start, end, _, _ = segment

    # 在选中的时间段内均匀随机
    span = (end - start).total_seconds()
    offset = random.random() * span
    return start + datetime.timedelta(seconds=int(offset))
