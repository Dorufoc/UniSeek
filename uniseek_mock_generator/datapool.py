def choice(pool):
    """
    从列表中随机返回一个元素。
    支持三种格式：
    - list: 等概率随机选取
    - list[tuple(item, weight)]: 按权重随机选取
    - dict: 从values中随机选取一个值
    """
    if isinstance(pool, dict):
        # 从字典中随机选择一个值
        key = random.choice(list(pool.keys()))
        return random.choice(pool[key])
    if isinstance(pool, list):
        if not pool:
            return None
        # 判断是否为带权重的元组列表
        if pool and isinstance(pool[0], (list, tuple)) and len(pool[0]) == 2:
            items, weights = zip(*pool)
            return random.choices(items, weights=weights, k=1)[0]
        return random.choice(pool)
    return None


def sample(pool, k=1):
    """
    从列表中随机返回 k 个不重复的元素。
    支持三种格式：
    - list: 等概率随机采样
    - list[tuple(item, weight)]: 按权重随机采样（不重复）
    - dict: 从values中随机采样
    """
    if isinstance(pool, dict):
        # 从字典中随机选取 k 个值
        keys = random.sample(list(pool.keys()), min(k, len(pool)))
        return [random.choice(pool[key]) for key in keys]
    if isinstance(pool, list):
        if not pool:
            return []
        if pool and isinstance(pool[0], (list, tuple)) and len(pool[0]) == 2:
            items, weights = zip(*pool)
            return random.choices(items, weights=weights, k=k)
        return random.sample(pool, min(k, len(pool)))
    return []


def pick_from_category(category_dict, category=None):
    """
    从分类字典中选取数据。
    若指定 category，则从该分类下选取；否则随机选一个分类。
    """
    if category:
        items = category_dict.get(category, [])
        return random.choice(items) if items else None
    cat = random.choice(list(category_dict.keys()))
    return random.choice(category_dict[cat])
